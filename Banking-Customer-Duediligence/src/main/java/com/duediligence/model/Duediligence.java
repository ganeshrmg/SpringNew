package com.duediligence.model;

public class Duediligence {
	
	private Integer creditScore;	
	private String dueDiligenceStatus;
	public Integer getCreditScore() {
		return creditScore;
	}
	public void setCreditScore(Integer creditScore) {
		this.creditScore = creditScore;
	}
	public String getDueDiligenceStatus() {
		return dueDiligenceStatus;
	}
	public void setDueDiligenceStatus(String dueDiligenceStatus) {
		this.dueDiligenceStatus = dueDiligenceStatus;
	}

}
